-- ROUND(number, decimal_places) — стандартное округление
-- Округляет число по математическим правилам (0.5 и выше → вверх, меньше 0.5 → вниз).
SELECT ROUND(4.7);     -- 5
SELECT ROUND(4.3);     -- 4
SELECT ROUND(-4.7);    -- -5
SELECT ROUND(-4.3);    -- -4
SELECT ROUND(4.756, 2); -- 4.76  (до 2 знаков после запятой)
SELECT ROUND(987.65, -2); -- 1000  (до сотен)

-- CEILING(number) — округление вверх
-- Всегда округляет число вверх до ближайшего целого.
SELECT CEILING(4.1);   -- 5
SELECT CEILING(4.9);   -- 5
SELECT CEILING(-4.1);  -- -4
SELECT CEILING(-4.9);  -- -4

-- FLOOR(number) — округление вниз
-- Всегда округляет число вниз до ближайшего целого.
SELECT FLOOR(4.9);    -- 4
SELECT FLOOR(4.1);    -- 4
SELECT FLOOR(-4.1);   -- -5
SELECT FLOOR(-4.9);   -- -5

-- ABS(number) — модуль числа
-- Возвращает абсолютное (модульное) значение числа, т.е. делает его неотрицательным.
SELECT ABS(10);   -- 10
SELECT ABS(-10);  -- 10
SELECT ABS(-4.7); -- 4.7
SELECT ABS(0);    -- 0

-- POW(base, exponent) (или POWER()) — возведение в степень
-- Возводит число (base) в заданную степень (exponent).
SELECT POW(2, 3);  -- 2^3 = 8
SELECT POWER(5, 2); -- 5^2 = 25
SELECT POW(9, 0.5); -- 9^(1/2) = 3 (квадратный корень)
SELECT POW(-2, 3); -- (-2)^3 = -8

-- SQRT(number) — квадратный корень
SELECT SQRT(25);  -- 5
SELECT SQRT(9);   -- 3
SELECT SQRT(2);   -- 1.4142...
SELECT SQRT(-4);  -- NULL (ошибка, так как число отрицательное)

-- RAND([seed]) — случайное число
-- Возвращает случайное число от 0 до 1 (включает 0, но не включает 1).
SELECT RAND();      -- Например, 0.678321...
SELECT RAND() * 100; -- Случайное число от 0 до 100 (включает 100)
SELECT FLOOR(RAND() * 100) + 1; -- Случайное целое число от 1 до 100 (включает 100)
SELECT RAND(10);    -- Повторяемый случайный результат (seed = 10)
SELECT RAND(10);    -- Повторно выдаст то же самое число

